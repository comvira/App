package LectureSix.gen.different;

public class PairIntString {
    private final Integer val1;
    private final String val2;

    public PairIntString(Integer val1, String val2) {
        this.val1 = val1;
        this.val2 = val2;
    }

    public Integer getVal1() {
        return val1;
    }

    public String getVal2() {
        return val2;
    }
}
