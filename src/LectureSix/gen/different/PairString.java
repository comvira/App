package LectureSix.gen.different;

public class PairString {
    private final String val1;
    private final String val2;

    public PairString(String val1, String val2) {
        this.val1 = val1;
        this.val2 = val2;
    }

    public String getVal1() {
        return val1;
    }

    public String getVal2() {
        return val2;
    }
}
